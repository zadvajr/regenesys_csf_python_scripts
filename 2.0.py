# Python Program for Area of Triangle:
b = float(input('Enter the base: '))
h = float(input('Enter the height: '))

# Calculate the area
area = 1/2 * b * h

print('The area of the triangle is %0.2f' % area)

# Output:
# Enter the base: 10
# Enter the height: 5
# The area of the triangle is 25.00